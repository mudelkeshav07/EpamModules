package Task2;

public class Bouquet {

    int rose;
    int jasmine;
    int lilys;

    public int getRose() {
        return rose;
    }

    public int getJasmine() {
        return jasmine;
    }

    public int getLilys() {
        return lilys;
    }

    public Bouquet(int rose, int jasmine, int lilys){
        this.rose = rose;
        this.jasmine = jasmine;
        this.lilys = lilys;
    }



}
